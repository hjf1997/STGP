def calc_metric(pred, y):

    B, N, L = pred.shape
    pred = pred.reshape(-1, L)
    y = y.reshape(-1, L)
    MSE = torch.mean((pred - y)**2, dim = 0)
    RMSE = torch.sqrt(MSE)
    MAE = torch.mean(torch.abs(pred - y), dim = 0)
    MAPE = torch.mean(torch.abs(pred - y) / y, dim = 0)
    return MSE, RMSE, MAE, MAPE